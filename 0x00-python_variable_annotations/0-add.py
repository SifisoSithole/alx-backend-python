#!/usr/bin/env python3
"""
This module contain the add function. The add function
returns the sum of two floats
"""


def add(a: float, b: float) -> float:
    """
    adds two floats

    args:
        a (float): first float
        b (float): second float

    return:
        float: the sum of the two floats
    """
    return a + b
